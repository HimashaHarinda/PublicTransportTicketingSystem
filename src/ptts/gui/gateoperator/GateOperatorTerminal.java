    private void loadTokenList(){
        ArrayList<Token> tempTokenList = TokenManager.getTokenList();

        if (tempTokenList != null) {
            String[] tokenIDList = new String[tempTokenList.size()];
            for (int i = 0; i < tempTokenList.size(); i++) {
                tokenIDList[i] = tempTokenList.get(i).getTokenID();
            }
            
            combo_homescreen_tokenIDList.setModel(new DefaultComboBoxModel(tokenIDList));
            button_homescreen_validate.setEnabled(true);
            
        }
        else{
            String[] tokenIDList = {"Empty List"};
            combo_homescreen_tokenIDList.setModel(new DefaultComboBoxModel(tokenIDList));
            button_homescreen_validate.setEnabled(false);
        }
    }
    
    
    
