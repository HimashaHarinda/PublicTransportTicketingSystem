    private String  generateReportID(){
        int reportCount;
        
        try {
            reportCount = CaseReportManager.getCaseReportList().size();
        } catch (NullPointerException e) {
            reportCount =0;
            System.out.println("Inspector Terminal [generate reportID]: Null token list!");
        }
        
        StringBuilder sb = new StringBuilder("CR");
        sb.append(Integer.toString(reportCount+1));
        
        return sb.toString();
    }
    
