    private void loadTokenList(){
        ArrayList<Token> tempTokenList = TokenManager.getTokenList();

        if (tempTokenList != null) {
            String[] tokenIDList = new String[tempTokenList.size()];
            for (int i = 0; i < tempTokenList.size(); i++) {
                tokenIDList[i] = tempTokenList.get(i).getTokenID();
            }
            
            combo_homescreen_tokenIDList.setModel(new DefaultComboBoxModel(tokenIDList));
            
            
        }
        else{
            String[] tokenIDList = {"Empty List"};
            combo_homescreen_tokenIDList.setModel(new DefaultComboBoxModel(tokenIDList));
            button_homescreen_validate.setEnabled(false);
        }
    }
    
    private void loadActiveTicketList(){
        ArrayList<Ticket> tempTicketList = currentToken.getActiveTicketList();

        if (tempTicketList != null && tempTicketList.size() != 0) {
            String[] ticketList = new String[tempTicketList.size()];
            for (int i = 0; i < tempTicketList.size(); i++) {
                ticketList[i] = tempTicketList.get(i).getTicketID();
            }
            
            combo_homescreen_ticketIDList.setModel(new DefaultComboBoxModel(ticketList));
            button_homescreen_validate.setEnabled(true);
            
        }
        else{
            String[] tokenIDList = {"Empty List"};
            combo_homescreen_ticketIDList.setModel(new DefaultComboBoxModel(tokenIDList));
            button_homescreen_validate.setEnabled(false);
            label_homescreen_tokenStatus.setText("Empty Token");
            label_homescreen_tokenStatus.setForeground(Color.RED);
            label_homescreen_tokenStatus.setFont(label_homescreen_tokenStatus.getFont().deriveFont(5));
            label_homescreen_tokenStatus.setVisible(true);
        }
    }
    
