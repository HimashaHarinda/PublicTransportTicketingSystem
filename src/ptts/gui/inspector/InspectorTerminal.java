    private void button_homescreen_sendReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_homescreen_sendReportActionPerformed
        // TODO add your handling code here:
        CaseReport newCaseReport = new CaseReport(reportID, inspectorID, tokenID, passengerID, station, calendar_date_time);
        if(CaseReportManager.addCaseReport(newCaseReport)){
            JOptionPane.showMessageDialog(null, "Case report " + reportID + " has been successfully submitted!", "Message", JOptionPane.INFORMATION_MESSAGE);
            disable_case_report_panel();
            button_homescreen_validate.setEnabled(false);
        }
        else{
            JOptionPane.showMessageDialog(null, "Case report " + reportID + " NOT submitted correctly!", "Error", JOptionPane.ERROR_MESSAGE);
            button_homescreen_validate.setEnabled(false);
        }
    }//GEN-LAST:event_button_homescreen_sendReportActionPerformed

