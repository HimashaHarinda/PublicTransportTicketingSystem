    private void button_homescreen_validateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_homescreen_validateActionPerformed
        // TODO add your handling code here:
        String station  = combo_homescreen_station.getSelectedItem().toString();
        String tokenID = combo_homescreen_tokenIDList.getSelectedItem().toString();
        try {
            Token currentToken = TokenManager.getTokenByID(tokenID);
            if(TokenValidator.validate(currentToken,station)){
                label_homescreen_tokenStatus.setText("Valid");
                label_homescreen_tokenStatus.setForeground(Color.GREEN);
                label_homescreen_tokenStatus.setVisible(true);
                button_homescreen_openGate.setEnabled(true);

            }
            else{
                label_homescreen_tokenStatus.setText("Invalid");
                label_homescreen_tokenStatus.setForeground(Color.RED);
                label_homescreen_tokenStatus.setVisible(true);

            }
        } catch (Exception e) {
            
            e.printStackTrace();
        }
    }//GEN-LAST:event_button_homescreen_validateActionPerformed

