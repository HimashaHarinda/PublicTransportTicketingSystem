    private void button_homescreen_validateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_homescreen_validateActionPerformed
        // TODO add your handling code here:
        station  = combo_homescreen_station.getSelectedItem().toString();
        tokenID = combo_homescreen_tokenIDList.getSelectedItem().toString();
        try {
            currentToken = TokenManager.getTokenByID(tokenID);
            if(TokenValidator.validate(currentToken,station)){
                label_homescreen_tokenStatus.setText("Valid");
                label_homescreen_tokenStatus.setForeground(Color.GREEN);
                label_homescreen_tokenStatus.setVisible(true);
                disable_case_report_panel();
            }
            else{
                label_homescreen_tokenStatus.setText("Invalid");
                label_homescreen_tokenStatus.setForeground(Color.RED);
                label_homescreen_tokenStatus.setVisible(true);
                
                reportID = generateReportID();
                inspectorID = currentInspector.getUserID();
                passengerID = currentToken.getPassengerID();
                
                calendar_date_time = Calendar.getInstance();
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                SimpleDateFormat timeFormat = new SimpleDateFormat("h:mm a");
                
                date = dateFormat.format(calendar_date_time.getTime());
                time = timeFormat.format(calendar_date_time.getTime());
                
                label_homescreen_reportID.setText(reportID);
                label_homescreen_inspectorID.setText(inspectorID);
                label_homescreen_station.setText(station);
                label_homescreen_passengerID.setText(passengerID);
                label_homescreen_tokenID.setText(tokenID);
                label_homescreen_date.setText(date);
                label_homescreen_time.setText(time);

                enable_case_report_panel();
            }
        } catch (Exception e) {
            
            e.printStackTrace();
        }
        
        
    }//GEN-LAST:event_button_homescreen_validateActionPerformed

