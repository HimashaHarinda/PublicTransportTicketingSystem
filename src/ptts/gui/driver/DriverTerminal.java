    private void button_checkout_checkoutConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_checkout_checkoutConfirmActionPerformed
        // TODO add your handling code here:
        String station= combo_checkout_station.getSelectedItem().toString();
        float distanceIncrement = Float.valueOf(text_checkout_distance.getText());
        
        CheckIn newCheckOut = new CheckIn(station, Calendar.getInstance(), 0);
        currentTicket.addCheckIn(newCheckOut);
        currentTicket.addDistance(distanceIncrement);
        System.out.println("Driver Temrinal: Check out added to the current[temp] ticket "+currentTicket.getTicketID());
        currentToken.removeTicketByID(currentTicket.getTicketID());
        currentToken.addTicket(currentTicket);
        
        //updating the ticket manager, token manager and passenger managers
        if(driverController.ticketCheckOutUpdates(currentTicket, currentToken, distanceIncrement)){
            JOptionPane.showMessageDialog(null,"Check out added to Ticket "+currentTicket.getTicketID()+" successfully", "Message", JOptionPane.INFORMATION_MESSAGE);
            combo_checkout_station.setEnabled(false);
            text_checkout_distance.setEnabled(false);
            button_checkout_checkoutConfirm.setEnabled(false);
            loadTokenList();
            String[] ticketIDList = {"Empty List"};
            combo_homescreen_ticketIDList.setModel(new DefaultComboBoxModel(ticketIDList));
            button_homescreen_validate.setEnabled(false);
            label_homescreen_tokenStatus.setVisible(false);
        }
        else{
            JOptionPane.showMessageDialog(null,"Check out NOT added to Ticket "+currentTicket.getTicketID()+" !", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_button_checkout_checkoutConfirmActionPerformed

